name = "abraham lincoln"

#converts the first letter of the word to upper case
print(name.title())

#converts all of the letters of the words to Upper case
print(name.upper())

#converts all of the letters of the words to lower case
print(name.lower())

#To add a tab to your text, use the character\t

print("Apples\tOranges")

#Add a newline, use the character \n
print("Apples\nOranges")

#rstrip removes white space from right end
#lstrip removes whitespace from left end
#strip removes whitespace from both ends
hello = " I am here "
print(hello.rstrip())
print(hello.lstrip())
print(hello.strip())